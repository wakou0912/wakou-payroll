export function calcWithholdingTax(taxableAmount: number, dependents: number): number {
  if (taxableAmount < 88000) return 0;
  const dep = Math.min(Math.max(0, Math.floor(dependents)), 7) as 0|1|2|3|4|5|6|7;
  const row = WITHHOLDING_TABLE.find(r => taxableAmount >= r.min && taxableAmount < r.max);
  if (!row) return 0;
  return row.tax[dep];
}

// 賞与の源泉徴収税計算（前月の給与額と扶養人数から概算）
export function calculateBonusIncomeTax(
  bonusAfterInsurance: number,
  prevMonthlySalaryAfterInsurance: number,
  dependents: number
): number {
  if (bonusAfterInsurance <= 0) return 0;
  const prevTax = calcWithholdingTax(prevMonthlySalaryAfterInsurance, dependents);
  const rate = prevMonthlySalaryAfterInsurance > 0
    ? prevTax / prevMonthlySalaryAfterInsurance
    : 0;
  return Math.floor(bonusAfterInsurance * rate);
}